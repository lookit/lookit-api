function clearChildren(element) {
    /*
    Remove all children from element.
     */
    while (element.firstChild) {
        element.removeChild(element.lastChild);
    }
}


const stateSelect = document.querySelector('#state-select');
const nothingToShow = document.querySelector("#nothing-to-show");
const chsLink = document.createElement('a')
const labList = document.querySelector('#lab-list');

let stateOption = document.createElement('option');
let labListItem;
let labLink;

// Add attributes to CHS link
chsLink.href = "https://childrenhelpingscience.com/";
chsLink.target = "_blank";
chsLink.innerHTML = "Children Helping Science"

// Start with empty option
stateSelect.append(stateOption);

// Populate select
for (let key in labsData) {
    stateOption = document.createElement('option');
    stateOption.value = stateOption.innerHTML = key;
    stateSelect.append(stateOption);
}

stateSelect.addEventListener('change', function () {
    // Set list header
    document.querySelector('.selected-state').innerHTML = stateSelect.value;

    // Clear children from previous selected
    clearChildren(nothingToShow);
    clearChildren(labList);

    if (labsData[stateSelect.value].length) {
        labsData[stateSelect.value].forEach(function (value) {
            labListItem = document.createElement('li');
            labLink = document.createElement('a');
            labLink.href = value.url;
            labLink.innerHTML = value.name;
            labListItem.append(labLink);
            labList.append(labListItem);
        })
    } else {
        nothingToShow.append(document.createElement('br'))
        nothingToShow.innerHTML += `Sorry, we don't know of any child development labs in ${stateSelect.value} yet! But you can also participate online at `
        nothingToShow.append(chsLink);
        nothingToShow.innerHTML += '.';
    }
});
